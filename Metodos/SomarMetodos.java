package Metodos;

public class SomarMetodos {
	
	int somar(int a, int b) {
		return a + b;
	}
	
	int somar(int a, int b, int c) {
		return a + b + c;
	}
	
	int media(int a, int b) {
		return (a + b) / 2;
	}
	
	float media(float a, float b, float c) {
		return  (a + b + c) / 3;
	}
}